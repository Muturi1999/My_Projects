// Helper function to get all products by brand
export function getProductsByBrand(brandSlug: string): Product[] {
  const allProducts = [
    ...hotDeals,
    ...laptopDeals,
    ...printerDeals,
    ...accessoriesDeals,
    ...audioDeals,
    ...Object.values(brandLaptops).flat(),
  ];
  
  // Filter by brand and remove duplicates based on product ID using Map for guaranteed uniqueness
  const productMap = new Map<string, Product>();
  
  allProducts.forEach((product) => {
    if (product.brand === brandSlug && !productMap.has(product.id)) {
      productMap.set(product.id, product);
    }
  });
  
  return Array.from(productMap.values());
}

